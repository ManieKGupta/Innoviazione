# Draw a star using turtle
import turtle as t
t.pencolor("red")
t.fillcolor("yellow")
t.begin_fill()
t.speed(20)
t.forward(95)
t.right(120)
t.forward(100)
t.left(45)
t.forward(95)
t.right(120)
t.forward(100)
t.left(45)
t.forward(95)
t.right(120)
t.forward(100)
t.left(45)
t.forward(95)
t.right(120)
t.forward(100)
t.left(45)
t.forward(95)
t.right(120)
t.forward(100)
t.left(45)
t.end_fill()

t.done()